package cs2114.mazesolver;

// -------------------------------------------------------------------------
/**
 * And enum that represents the current mode of the screen. It contains the four
 * possible modes of drawing for the Maze.
 *
 * @author Benjamin Kodres-O'Brien (bkobrien)
 * @version 2013.1025
 */

public enum Mode
{
    /**
     * The draw mode.
     */
    DRAW,
    /**
     * The erase mode.
     */
    ERASE,
    /**
     * The set goal mode.
     */
    SET_GOAL,
    /**
     * The set start mode.
     */
    SET_START;
}
